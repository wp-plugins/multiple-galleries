add_action('admin_enqueue_scripts', 'add_multiple_galleries_js');
function add_multiple_galleries_js($where) {
	if ($where == 'media-upload-popup')
		wp_enqueue_script('multiple-galleries', plugins_url('multiple-galleries/multiple-galleries.js'), array('jquery', 'media-upload', 'utils'), 1.0, true);
